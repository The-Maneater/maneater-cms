        <h2>Basics</h2>

        <p>The Maneater is the independent, student-run newspaper at MU. It covers issues mostly relating to students and aims to serve primarily a campus readership. It is distributed throughout campus and the city on Wednesday.</p>

        <p>The Missourian is operated by the Missouri School of Journalism. Students work staff positions through classes they take and there are faculty editors who oversee the editing and production process. It is distributed Tuesday through Friday and on Sunday. It is one of two daily community newspapers, the other being the Columbia Daily Tribune.</p>

