        <h2>Eligibility and Staff</h2>

        <p>To work for The Maneater, you must be an MU student enrolled in a minimum of six credit hours. You do not have to be a journalism major. Typically, the staff is composed of freshman and sophomores, with some upperclassmen.</p>

        <p>To work for the Missourian, you must be an MU journalism student enrolled in a staff class or taking an independent study. Staff classes including reporting, copy editing, design, photography, infographics and more. This is a class in which one must enroll as a part of their required sequence courses, and tuition is charged for taking the class. Typically, upperclassmen enroll in these classes once they enter their sequence.</p>

        <h2>Pay</h2>

        <p>The Maneater pays some of its staff varying amounts for their work. Editors are given a monthly salary. The Maneater also has a business adviser.</p>

        <p>The Missourian does not pay students who are taking classes through the School of Journalism. It pays faculty editors, who also serve as professors at the journalism school, and certain student positions.</p>

        <h2>Funding</h2>

        <p>The Maneater is funded entirely through its advertising revenue. It does not receive any funding from the university or through student fees. It is entirely independent from the university.</p>

        <p>The Missourian receives funding from MU's budget.</p>

        <h2>Location</h2>

        <p>The Maneater is located in G216 MU Student Center.</p>

        <p>The Missourian operates out of Lee Hills Hall, at the corner of Elm and Eighth streets.</p>

