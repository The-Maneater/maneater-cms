        <h2>Staff Writers</h2>
        <p>All writers start as reporters. A reporter can become a staff writer by completing ten story assignments. When a staff writer has worked with The Maneater for two semesters, he or she is then considered a senior staff writer. Beat writers are also hired at the beginning of every semester to cover certain topics within each section.</p>
        <h2>Photographers</h2>
        <p>A photographer becomes a staff photographer by completing ten photo assignments. After two semesters of working for The Maneater, a staff photographer becomes a senior staff photographer. Though most photographers have their own cameras, it is not essential.</p>
        <h2>Production Staff</h2>
        <p>The production staff works in the evening on production night (Tuesday night) to arrange the stories, photos and graphics on the pages to be printed. The Maneater uses Adobe InDesign for page layouts and Adobe Illustrator for graphics.</p>
        <h2>Columnists</h2>
        <p>Anyone wishing to be a columnist can apply at the end of a semester. Watch for posters and house ads in The Maneater announcing that applications are available. The editorial board then chooses the next semester's columnists from the submitted applications. Each columnist appears once a week and is therefore responsible for turning in one column per week.</p>
        <h2>Advertising Representatives</h2>
        <p>Advertising representatives work with the business manager to sell print and online ads from local businesses, contact our business manager Colin Kreager at ckreager@themaneater.com if you are interested.</p>
