        <h2>An Equal Opportunity Employer</h2>
        <p>The Maneater is eager to welcome any new staff member, regardless of previous experience or college major. In other words, you don't have to be a (pre-)journalism major nor do you have to have worked for a newspaper before. Our staff stem from a diversity of backgrounds and vary in involvement level. Time commitments are determined by individual level of involvement.</p>
        <p>As an official but independent student paper of MU, The Maneater is funded solely by advertisements, and students control all editorial aspects of the content and production process. As such, The Maneater experiences a relatively quick staff turnover, meaning we're always enthused to see new, rising talent.</p>
