        <h2>Policies</h2>

        <h4>Requests to Revise/Remove Content</h4>

        <p>In addition to publishing online-exclusive content, The Maneater’s website acts as a digital archive of content printed in issues of its newspaper. Thorough archives since the website’s most recent iteration in February 2008 are available as well as intermittent prior content published retroactively. This content is retained freely and publicly for the benefit of the publication, its readers and its former staff. The Maneater will not retract any of this content unless it is found to be plagiarized or libelous.</p>

        <p>It will — upon the request of a concerned party or of its own discovery — correct information, particularly within articles, found to be inaccurate or misleading at the time of publication. Corrections to stories printed in its newspaper will be immediately appended to the archived story online and identified in the next issue of the newspaper on page 2. Corrections to online-exclusive content will also be immediately updated and indicated on its present/archived web page. Changes and updates to content which was correct at the time of publication but later rendered inaccurate will be administered on a case-by-case basis at the discretion of the publication's current editor-in-chief.</p>

        <p>In making corrections, in addition to retracting or revising text-based content, The Maneater attempts to distinguish such changes by appending an additional field describing the revisions and appending a date and time modified stamp to its articles. However, because web pages are frequently archived by outside sites — including many search engines — and cached by individuals’ web browsers, The Maneater cannot guarantee that any information revised or deleted will be permanently removed from the web nor that effected changes will immediately be reflected to all web users.</p>

        <h4>Comments</h4>

        <p>Articles, editorials, columns, blog posts and multimedia content on The Maneater's website are open to public comments. This is intended to be a forum for reader reaction, input and related discussion. Anyone may post a comment. Registration or log in is not required to submit a comment. Anonymous comments are accepted, but proper names are preferred. Comments will be moderated before appearing on the site. While The Maneater will not censor opinions or language, unsubstantiated posts of malice or those deliberately promoting external links unrelated to the subject will be rejected.</p>

        <h4>Letters to the editor</h4>

        <p>The Maneater strives to encourage discussion among the MU community. Generally, any letter from students, faculty, staff, alumni and extended community will be considered for publication. In addition, those of topical significance to the university and its students will also be considered. Letters can be submitted from <a href="#" onclick="sendLetter()">the website</a>, emailed to <a href="forum@themaneater.com">forum@themaneater.com</a>, faxed to 573.882.5550 or dropped off at G216 MU Student Center. Submissions must include the writers' phone numbers and/or email addresses. Most letters must be signed to be published; anonymity will be considered in a small number of cases, where necessary. The Maneater reserves the right to edit letters and columns for style and length.</p>

        <br>

        <h2>Privacy</h2>

        <h4>Using the site</h4>

        <p>With the exception of letters to the editor, The Maneater collects contact information for its use only. While contact information fields — such as, but not limited to, email address, phone number and mailing address — may be required for certain account registration, comments and email submissions, such information will not be published nor released to any third party. This information is collected for identification purposes, where necessary, and enables The Maneater to accurately and promptly follow-up to postings, when necessary. Falsifying this information will invalidate all information associated with it. Comments, letters to the editor and feedback will be dismissed accordingly. Accounts may be terminated indefinitely.</p>

        <p>Requests for anonymity in published letters to the editor will be considered by the editor-in-chief when a letter contains sensitive information that would be damaging to the writer's well-being. Accurate contact information must still be provided to the editor-in-chief when the letter is submitted. Requests for anonymity will be determined after the writer's authenticity is confirmed. Traditionally, The Maneater will publish the writer's name and email address at the end of the letter. Letters written by multiple people or on behalf of an organization will be signed by all those who have written or otherwise endorsed the writing.</p>

        <h4>In the blotter</h4>

        <p>Within most issues of the newspaper, The Maneater publishes a police blotter. This is compiled by The Maneater staff from the arrest records of the MU Police Department and the Columbia Police Department. These are public records. Frequently, The Maneater is asked to remove entries filed under its blotter archives. Adhering to the aforementioned revision and retraction policy, The Maneater will not delete or otherwise alter these entries unless the information contained, particularly persons identified and charges filed, prove to be incorrect at time of publication. However, provided relevant documentation, The Maneater will update an entry to indicate if a charge was later dismissed, reduced or acquitted.</p>

