        <h2>Accuracy</h2>

        <p>The Maneater is dedicated to accurate reporting. All of our reporters are required to double-check quotes with their sources prior to publication. If you were quoted in a Maneater report and weren't contacted for a quote check, or if you notice an inaccuracy, please contact the editors at <a href="editors@themaneater.com">editors@themaneater.com</a>.</p>


